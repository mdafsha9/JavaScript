//for of loop & for in loop

//for of loop

let fruits = ["Apple", "Mango", "Pineapple"];
for(let fruit of fruits){
  console.log(fruit);
}

//for in loop, returns index of array
let vegies = ["Cabbage", "Chilli", "Beans"];
for(let index in vegies){
  console.log(index);
  console.log(vegies[index]);
}
