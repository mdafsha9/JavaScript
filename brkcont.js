//break keywords---it break the execution where we used break keyword


for(var i=1; i<=10; i++){
  if(i === 4){
    break;
  }
  console.log(i);
}

//continue keywords--continue skip the number where we used continue keyword
for(var i=1; i<=10; i++){
  if(i === 4){
    continue;
  }
  console.log(i);
}
